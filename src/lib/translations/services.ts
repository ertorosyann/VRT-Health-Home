export function getServicesTranslation(locale: Locale, key: string) {
  const translations = servicesTranslations[locale]
  const keys = key.split('.')
  let value: any = translations
  
  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = value[k]
    } else {
      return key
    }
  }
  
  return value
}
